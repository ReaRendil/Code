import java.util.*;
public class Main {
    public static void main (String[] args) {
        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        int q;
        int w;
        for  (int a =0; a<t; a++){
            q = sc.nextInt();
            w = sc.nextInt();
            System.out.println(q+w);
        }
    }
}